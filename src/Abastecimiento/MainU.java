    private void printRegis(JPanel panelT){
       PrinterJob job = PrinterJob.getPrinterJob();
       job.setJobName("job Test");
       job.setPrintable((Graphics graphics, PageFormat pageFormat, int pageIndex) -> {
           if(pageIndex>0){
               return Printable.NO_SUCH_PAGE;
           }
           Graphics2D g2d = (Graphics2D)graphics;
           g2d.translate(pageFormat.getImageableX()*2, pageFormat.getImageableY()*2);
           g2d.scale(0.5,0.5);
           
           panelT.paint(g2d);
           return Printable.PAGE_EXISTS;
       });
       
       boolean returnResult=job.printDialog();
       if(returnResult){
           try {
               job.print();
           } catch (PrinterException e) {
               JOptionPane.showMessageDialog(this,"print error" +e.getMessage());
           }
       }
    }
