    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
/*          try {
            mytable.print(); // Imprime el jTable
        } catch (PrinterException ex) { }
        */

    try {

      Document documento = new Document();

      String ruta = System.getProperty("user.home");
      PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/OneDrive/Escritorio/" + "CalculosConsumo" + ".pdf"));
      com.itextpdf.text.Image header = com.itextpdf.text.Image.getInstance("src/imagenes/USMP-2020.png");
      header.scaleToFit(200, 400);
      header.setAlignment(Chunk.ALIGN_CENTER);
      Paragraph parrafo = new Paragraph();
      parrafo.setAlignment(Paragraph.ALIGN_CENTER);
      parrafo.add("Tabla.2 Resultado de Calculos de variacion de consumo \n \n");
      parrafo.setFont(FontFactory.getFont("Tahoma", 18, Font.BOLD, BaseColor.DARK_GRAY));
      titulo= new Paragraph("Universidad De San Martin De Porres");
      documento.open();
       titulo.setAlignment(1);
      documento.add(titulo);
      
      documento.add(header);
      documento.add(parrafo);
      
       
      
        PdfPTable tabla = new PdfPTable(9);
        
            tabla.addCell("Codigo");
            tabla.addCell("Año");
            tabla.addCell("Poblacion");
            tabla.addCell("Consumo(L/hab.d)");
            tabla.addCell("Qm (L/s)");
            tabla.addCell("Factor mayoración");
            tabla.addCell("Qmd (L/s)");
            tabla.addCell("Qmh (L/s)");
            tabla.addCell("Qmm (L/s)");

            
         for(TablaConsumo TC: this.tablaconsumo){
                tabla.addCell(TC.getCodigo()); 
                tabla.addCell(TC.getAno());
                tabla.addCell(TC.getPoblacion());
                tabla.addCell(TC.getConsumo());
                tabla.addCell(TC.getQm());
                tabla.addCell(TC.getFactormayoracion());
                tabla.addCell(TC.getQmd());
                tabla.addCell(TC.getQmh());
                tabla.addCell(TC.getQmm());
            }
         documento.add(tabla);
         documento.close();

         
    JOptionPane.showMessageDialog(null, "Reporte creado correctamente.");
      }catch(DocumentException | IOException e){
      System.err.println("Error en PDF o ruta de imagen" + e);
      JOptionPane.showMessageDialog(null, "Error al generar PDF");
      
      } 

    }//GEN-LAST:event_jButton2ActionPerformed

