    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
            //-------Decalracion de variables-------//
         if (txt_ano.getText().isEmpty() || txt_poblacion.getText().isEmpty()){
             getToolkit().beep();
             JOptionPane.showMessageDialog(null,"Por favor, llenar los campos");
         }else {
             int ano = 0; 
            double poblacion = 0.0;
            String k1 = "",k2 = "";
            double consumo,Qm,Qmd, factormayoracion = 1.50, Qmh,Qmm;

            ano = Integer.valueOf(txt_ano.getText());
            poblacion = Double.valueOf(txt_poblacion.getText());
            k1 =  txt_k1.getSelectedItem().toString();
            k2 = txt_k2.getSelectedItem().toString();

            data.generate_information(ano, poblacion, k1, k2);

            consumo = data.getConsumo();
            Qm = data.getQm();
            Qmd = data.getQmd();
            Qmh = data.getQmh();
            Qmm = data.getQmm();
            int codigo = data.getContador();
            
            TablaConsumo TC1 = new TablaConsumo();
            
               TC1.setCodigo(String.valueOf(codigo));
               TC1.setAno(String.valueOf(ano));
               TC1.setPoblacion(String.valueOf(poblacion));
               TC1.setConsumo(String.valueOf(consumo));
               TC1.setQm(String.valueOf(Qm));
               TC1.setFactormayoracion(String.valueOf(factormayoracion));
               TC1.setQmd(String.valueOf(Qmd));
               TC1.setQmh(String.valueOf(Qmh));
               TC1.setQmm(String.valueOf(Qmm));
     
              tablaconsumo.add(TC1);
            
            model = (DefaultTableModel) data.datos_Tabla(model,codigo,ano,poblacion,consumo,Qm,Qmd,Qmh,Qmm, factormayoracion);
            mytable.setModel(model);
             
            txt_k2.setSelectedItem(ano);
         }
         

    }//GEN-LAST:event_BtnGuardarActionPerformed

